package Ejercicios;

public class funciones1 {

		//esta funjcion hace de kilometros a millas
	public static double kilometrosamillas(double km) {
		double m=km*0.621371;

		
		return m;
	}
	
	//esta funcion hace de millas a kilometros
	public static double millasakilometros(double m) {
		double km=m*1.60934;
		
		return km;
	}
	
	//esta funcion hace de kilogramos a libras
	public static double kilogramosalibras(double kg) {
		double lb=kg*2.2046;
		
		
		return lb;
	}
	
	//esta funcion hace de libras a kilogramos
	public static double librasakilogramos(double lb) {
		double kg=lb*0.453592;
		
		
		return kg;
	}

}
